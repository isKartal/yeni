// Arama sistemi kurulumu
function setupSearchSystem() {
    const searchInput = document.getElementById('search-input');
    const searchBtn = document.getElementById('search-btn');
    
    if (searchInput) {
        // Anlƒ±k arama √∂nerileri
        searchInput.addEventListener('input', function() {
            clearTimeout(searchTimeout);
            const query = this.value.trim();
            
            if (query.length >= 2) {
                searchTimeout = setTimeout(() => {
                    loadSearchSuggestions(query);
                }, 300);
            } else {
                hideSuggestions();
            }
        });
        
        // Enter tu≈üu ile arama
        searchInput.addEventListener('keypress', function(e) {
            if (e.key === 'Enter') {
                e.preventDefault();
                performSearch();
            }
        });
        
        // Focus √ßƒ±kƒ±nca √∂nerileri gizle (biraz gecikme ile)
        searchInput.addEventListener('blur', function() {
            setTimeout(hideSuggestions, 200);
        });
    }
    
    // Filtre deƒüi≈üikliklerini dinle
    const filterInputs = ['min-price', 'max-price', 'in-stock-only', 'sort-by'];
    filterInputs.forEach(id => {
        const element = document.getElementById(id);
        if (element) {
            element.addEventListener('change', applyFilters);
        }
    });
}

// Arama √∂nerilerini y√ºkle
async function loadSearchSuggestions(query) {
    try {
        const response = await fetch(`http://127.0.0.1:8000/api/search/suggestions/?q=${encodeURIComponent(query)}`);
        const data = await response.json();
        
        displaySuggestions(data.suggestions);
    } catch (error) {
        console.error('√ñneriler y√ºklenemedi:', error);
    }
}

// √ñnerileri g√∂ster
function displaySuggestions(suggestions) {
    const suggestionsContainer = document.getElementById('search-suggestions');
    
    if (suggestions.length === 0) {
        hideSuggestions();
        return;
    }
    
    suggestionsContainer.innerHTML = '';
    
    suggestions.forEach(suggestion => {
        const suggestionItem = document.createElement('div');
        suggestionItem.className = 'suggestion-item';
        suggestionItem.textContent = suggestion;
        suggestionItem.addEventListener('click', () => {
            document.getElementById('search-input').value = suggestion;
            hideSuggestions();
            performSearch();
        });
        suggestionsContainer.appendChild(suggestionItem);
    });
    
    suggestionsContainer.style.display = 'block';
}

// √ñnerileri gizle
function hideSuggestions() {
    const suggestionsContainer = document.getElementById('search-suggestions');
    if (suggestionsContainer) {
        suggestionsContainer.style.display = 'none';
    }
}

// Arama yap
function performSearch() {
    const query = document.getElementById('search-input').value.trim();
    currentSearchQuery = query;
    currentPage = 1;
    
    if (query.length === 0) {
        clearSearch();
        return;
    }
    
    hideSuggestions();
    hideAllSections();
    
    const searchSection = document.getElementById('search-results-section');
    if (searchSection) {
        searchSection.style.display = 'block';
        loadSearchResults();
    }
}

// Arama sonu√ßlarƒ±nƒ± y√ºkle
async function loadSearchResults(append = false) {
    try {
        // Filtrelerileri al
        const minPrice = document.getElementById('min-price')?.value || '';
        const maxPrice = document.getElementById('max-price')?.value || '';
        const inStockOnly = document.getElementById('in-stock-only')?.checked || false;
        const sortBy = document.getElementById('sort-by')?.value || 'newest';
        
        // URL olu≈ütur
        const params = new URLSearchParams({
            q: currentSearchQuery,
            page: currentPage,
            sort: sortBy
        });
        
        if (minPrice) params.append('min_price', minPrice);
        if (maxPrice) params.append('max_price', maxPrice);
        if (inStockOnly) params.append('in_stock', 'true');
        
        const response = await fetch(`http://127.0.0.1:8000/api/search/?${params}`);
        const data = await response.json();
        
        displaySearchResults(data, append);
    } catch (error) {
        console.error('Arama sonu√ßlarƒ± y√ºklenemedi:', error);
        document.getElementById('search-results-container').innerHTML = 
            '<p class="error">Arama sonu√ßlarƒ± y√ºklenirken hata olu≈ütu.</p>';
    }
}

// Arama sonu√ßlarƒ±nƒ± g√∂ster
function displaySearchResults(data, append = false) {
    const container = document.getElementById('search-results-container');
    const infoContainer = document.getElementById('search-info');
    const loadMoreContainer = document.getElementById('load-more-container');
    const titleContainer = document.getElementById('search-results-title');
    
    // Ba≈ülƒ±k g√ºncelle
    if (titleContainer) {
        if (currentSearchQuery) {
            titleContainer.textContent = `"${currentSearchQuery}" i√ßin arama sonu√ßlarƒ±`;
        } else {
            titleContainer.textContent = 'Filtrelenmi≈ü sonu√ßlar';
        }
    }
    
    // Bilgi g√∂ster
    if (infoContainer) {
        infoContainer.textContent = `${data.total_count} sonu√ß bulundu`;
    }
    
    // Sonu√ßlarƒ± g√∂ster
    if (!append) {
        container.innerHTML = '';
    }
    
    if (data.results.length === 0 && !append) {
        container.innerHTML = '<p class="no-results">Arama kriterlerinize uygun sonu√ß bulunamadƒ±.</p>';
        loadMoreContainer.style.display = 'none';
        return;
    }
    
    // Kitap kartlarƒ± olu≈ütur
    data.results.forEach(book => {
        const bookCard = createBookCard(book);
        container.appendChild(bookCard);
    });
    
    // Daha fazla y√ºkle butonu
    if (data.has_more) {
        loadMoreContainer.style.display = 'block';
    } else {
        loadMoreContainer.style.display = 'none';
    }
}

// Kitap kartƒ± olu≈ütur (displayBooks'taki ile aynƒ± mantƒ±k)
function createBookCard(book) {
    const bookCard = document.createElement('div');
    bookCard.className = 'book-card';
    
    // Stok durumu kontrol√º
    const isOutOfStock = book.stock <= 0;
    const isLowStock = book.stock > 0 && book.stock <= 3;
    const isVeryLowStock = book.stock === 1;
    
    let stockWarning = '';
    let buttonContent = '';
    
    if (isOutOfStock) {
        stockWarning = '<div class="stock-warning out-of-stock">‚ùå Stokta Yok</div>';
        buttonContent = '<button class="add-to-cart-btn disabled" disabled>Stokta Yok</button>';
    } else if (isVeryLowStock) {
        stockWarning = '<div class="stock-warning very-low-stock">üî• Son 1 adet!</div>';
        buttonContent = `<button class="add-to-cart-btn urgent" onclick="addToCart(${book.id})">Hemen Al!</button>`;
    } else if (isLowStock) {
        stockWarning = '<div class="stock-warning low-stock">‚ö° T√ºkenmek √úzere!</div>';
        buttonContent = `<button class="add-to-cart-btn" onclick="addToCart(${book.id})">Sepete Ekle</button>`;
    } else {
        stockWarning = '<div class="stock-info">‚úÖ Stokta Mevcut</div>';
        buttonContent = `<button class="add-to-cart-btn" onclick="addToCart(${book.id})">Sepete Ekle</button>`;
    }
    
    // Satƒ±cƒ± bilgisi
    const sellerInfo = book.seller_name ? `<div class="seller-info">Satƒ±cƒ±: ${book.seller_name}</div>` : '';
    
    bookCard.innerHTML = `
        <div class="book-title">${book.title}</div>
        <div class="book-author">Yazar: ${book.author}</div>
        <div class="book-price">${book.price} TL</div>
        <p>${book.description}</p>
        ${sellerInfo}
        ${stockWarning}
        ${buttonContent}
    `;
    
    if (isOutOfStock) {
        bookCard.classList.add('out-of-stock-card');
    } else if (isVeryLowStock) {
        bookCard.classList.add('urgent-stock-card');
    }
    
    return bookCard;
}

