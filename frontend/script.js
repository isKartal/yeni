async function handleCheckoutSubmit(e) {
    e.preventDefault();
    
    const shippingAddress = document.getElementById('shipping-address').value.trim();
    const phone = document.getElementById('phone').value.trim();
    
    // Validation
    if (!shippingAddress || shippingAddress.length < 10) {
        alert('L√ºtfen ge√ßerli bir teslimat adresi giriniz! (En az 10 karakter)');
        return;
    }
    
    if (!phone || phone.length < 10) {
        alert('L√ºtfen ge√ßerli bir telefon numarasƒ± giriniz!');
        return;
    }
    
    // Telefon numarasƒ± formatƒ± kontrol√º
    const phonePattern = /^(\+90|0)?[5][0-9]{2}[0-9]{3}[0-9]{4}$/;
    const cleanedPhone = phone.replace(/[\s\-\(\)]/g, '');
    
    if (!phonePattern.test(cleanedPhone)) {
        alert('L√ºtfen ge√ßerli bir T√ºrkiye telefon numarasƒ± giriniz!\n√ñrnek: 0555 123 45 67');
        return;
    }
    
    if (cart.length === 0) {
        alert('Sepetiniz bo≈ü!');
        return;
    }
    
    // Loading g√∂ster
    const submitBtn = e.target.querySelector('button[type="submit"]');
    const originalText = submitBtn.textContent;
    submitBtn.textContent = 'ƒ∞≈üleniyor...';
    submitBtn.disabled = true;
    
    try {
        const cartItems = cart.map(item => ({
            book_id: item.id.toString(),
            quantity: item.quantity.toString()
        }));
        
        const response = await fetch('http://127.0.0.1:8000/api/orders/create/', {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json',
                'Authorization': `Bearer ${getToken()}`
            },
            body: JSON.stringify({
                shipping_address: shippingAddress,
                phone: cleanedPhone,
                cart_items: cartItems
            })
        });
        
        const data = await response.json();
        
        if (response.ok) {
            alert('Sipari≈ü ba≈üarƒ±yla olu≈üturuldu! Sipari≈ü numaranƒ±z: #' + data.order.id);
            cart = []; // Sepeti temizle
            updateCartDisplay();
            
            // Formu temizle
            document.getElementById('checkout-form').reset();
            
            // 2 saniye sonra sipari≈ülere y√∂nlendir
            setTimeout(() => {
                showOrders();
            }, 2000);
        } else {
            alert('Hata: ' + (data.error || 'Sipari≈ü olu≈üturulamadƒ±'));
        }
    } catch (error) {
        console.error('Order creation error:', error);
        alert('Baƒülantƒ± hatasƒ±! L√ºtfen tekrar deneyin.');
    } finally {
        // Loading'i kaldƒ±r
        submitBtn.textContent = originalText;
        submitBtn.disabled = false;
    }
}

